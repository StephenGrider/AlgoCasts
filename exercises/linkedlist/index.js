class Node {
	constructor(data, next = null) {
		this.data = data;
		this.next = next;
	}
}

class LinkedList {
	constructor() {
		this.head = null;
	}

	insertFirst(data) {
		const first = new Node(data)
		this.head = first
	}
}

module.exports = { Node, LinkedList };
